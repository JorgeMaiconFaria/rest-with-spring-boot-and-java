package com.github.jorgemaicon.math;

public class SimpleMath {

    static public Double sum(Double numberOne, Double numberTwo) {
        return numberOne + numberTwo;
    }

    static public Double subtract(Double numberOne, Double numberTwo) {

        return numberOne - numberTwo;
    }

    static public Double multiplication(Double numberOne, Double numberTwo) {

        return numberOne * numberTwo;
    }

    static public Double division(Double numberOne, Double numberTwo) {

        return numberOne / numberTwo;
    }

    static public Double squareRoot(Double numberOne) {

        return Math.sqrt(numberOne);
    }

    static public Double mean(Double numberOne, Double numberTwo) {

        return (numberOne + numberTwo) / 2;
    }
}
